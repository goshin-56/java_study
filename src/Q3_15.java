
//１から100までの数値を乱数で発生させて表示し、以下の処理を行いなさい。
//
//80点以上なら”優”と表示。
//80点未満、70点以上なら、”良”と表示。
//70点未満、60点以上なら、”可”と表示。
//60点未満なら、”不可”と表示。

import java.util.Random;

public class Q3_15 {
	public static void main (String[] args) {
		
		Random a = new Random();
		int b = a.nextInt(100)+1;
		
		System.out.println(b+"点");
		if (b >= 80) {
			System.out.println("優");
		} else if (b >= 70 && b < 80) {
			System.out.println("良");
		} else if (b >= 60 && b < 70) {
			System.out.println("可");
		} else {
			System.out.println("不可");
		}
	}
}